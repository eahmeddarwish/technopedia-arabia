const SUPPORTED_LANGS = ["ar", "en"];
const DEFAULT_LANG = "ar";
const LANG_STORAGE_KEY = "ta_lang";

function getStoredLang() {
  try {
    const stored = window.localStorage.getItem(LANG_STORAGE_KEY);
    if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
  } catch (e) {
    /* localStorage unavailable */
  }
  return DEFAULT_LANG;
}

function t(key, lang) {
  const activeLang = lang && SUPPORTED_LANGS.includes(lang) ? lang : getStoredLang();
  const dict = translations[activeLang] || translations[DEFAULT_LANG];
  if (dict && Object.prototype.hasOwnProperty.call(dict, key)) return dict[key];
  const fallback = translations[DEFAULT_LANG];
  return (fallback && fallback[key]) || key;
}

function applyTranslations(lang) {
  const activeLang = SUPPORTED_LANGS.includes(lang) ? lang : DEFAULT_LANG;
  document.documentElement.setAttribute("lang", activeLang);
  document.documentElement.setAttribute("dir", activeLang === "ar" ? "rtl" : "ltr");

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = t(key, activeLang);
    if (value !== undefined) el.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.setAttribute("placeholder", t(key, activeLang));
  });

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === activeLang);
  });

  document.title = t("meta.title", activeLang);

  document.dispatchEvent(new CustomEvent("ta:langchange", { detail: { lang: activeLang } }));
}

function setLang(lang) {
  const activeLang = SUPPORTED_LANGS.includes(lang) ? lang : DEFAULT_LANG;
  try {
    window.localStorage.setItem(LANG_STORAGE_KEY, activeLang);
  } catch (e) {
    /* localStorage unavailable */
  }
  applyTranslations(activeLang);
}

function initI18n() {
  applyTranslations(getStoredLang());

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.addEventListener("click", () => setLang(btn.getAttribute("data-lang")));
  });
}

if (typeof document !== "undefined") {
  document.addEventListener("DOMContentLoaded", initI18n);
}

if (typeof window !== "undefined") {
  window.t = t;
  window.setLang = setLang;
  window.applyTranslations = applyTranslations;
  window.getStoredLang = getStoredLang;
}
